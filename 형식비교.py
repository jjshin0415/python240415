# 리스트 (List)
numbers = [1, 2, 3, 4, 5]
print(numbers)  # 출력: [1, 2, 3, 4, 5]

# 튜플 (Tuple)
numbers = (1, 2, 3, 4, 5)
print(numbers)  # 출력: (1, 2, 3, 4, 5)

# 세트 (Set)
numbers = {1, 2, 3, 4, 5}
print(numbers)  # 출력: {1, 2, 3, 4, 5} (순서는 보장되지 않음)

# 딕셔너리 (Dict)
numbers = {"one": 1, "two": 2, "three": 3, "four": 4, "five": 5}
print(numbers)  # 출력: {'five': 5, 'four': 4, 'one': 1, 'three': 3, 'two': 2} (키-값 쌍으로 저장)
